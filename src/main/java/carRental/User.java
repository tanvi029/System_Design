package carRental;

public class User {
    int userId;
    String userName;
    int drivingLicensce;

    public int getUserId() {
        return userId;
    }

    public String getUserName() {
        return userName;
    }

    public int getDrivingLicensce() {
        return drivingLicensce;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setDrivingLicensce(int drivingLicensce) {
        this.drivingLicensce = drivingLicensce;
    }
}
